    //<bloco> ::= begin <sentencas> end
    public void bloco(){
        if(comparaClasseLexema("cRes","begin")){
                lexema = analisadorLexico(texto);
                sentencas();
                if(comparaClasseLexema("cRes","end")){
                        lexema = analisadorLexico(texto);
                } else {
                        imprimeErro("cRes","end");
                }
        } else {
                imprimeErro("cRes", "begin");
        }
    }
        
