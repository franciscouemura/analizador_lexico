    public File geraArquivoAsm(){
        File file = new File("./programa.asm");
        try{
            file.createNewFile();
            FileWriter asm = new FileWriter(file);
            asm.write(codigoAssembly); // colocar a string do assembly aqui
            asm.flush();
            asm.close();
        }catch(Exception ex){
        }
        
        return file;
    }

